    public HeaderType setHeader() {
        HeaderType header = new HeaderType(); //headerType 객체 생성
        header = eventPanel1.setEvent(); //eventPanel의 데이터를 불러오기 위해 EventPanel의 setEvent method를 호출
      
        header.setEffective(stringToXMLGregorianCalendar(effectiveTextField.getText())); //effective에 데이터를 입력하기 위해 string을 XMLGregorianCalendar로 변환
        header.setExpires(stringToXMLGregorianCalendar(expiresTextField.getText())); //expires 데이터를 입력하기 위해 string을 XMLGregorianCalendar로 변환
        header.getLocation().add(locationPanel2.setLocation()); //location의 데이터를 불러오기 위해 LocationPanel의 setLocation method 호출
        
        return header;
    }
    
    //함수명 : stringToXMLGregorianCalendar
    //입력 : String
    //출력 : XMLGregorianCalendar
    //header의 expire와 effective에 데이터를 입력하기 위해 String 자료형을 XMLGregorianCalendar 자료형으로 변환
    private XMLGregorianCalendar stringToXMLGregorianCalendar(String s) {
        XMLGregorianCalendar result = null; //XMLGregorianCalendar 객체 생성
        try {
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss"); //date format 설정
            Date date = simpleDateFormat.parse(s); //date 객체 생성
            GregorianCalendar gregorianCalendar = (GregorianCalendar) GregorianCalendar.getInstance(); //gregorianCalendar 객체 생성
            gregorianCalendar.setTime(date); //gregorianCalendar 의 날짜 설정
            result = DatatypeFactory.newInstance().newXMLGregorianCalendar(gregorianCalendar); //xmlgregorianclanedar 저장
        } catch (ParseException ex) {
            //Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DatatypeConfigurationException ex) {
            //Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return result;
    }
    
