    public void loadAEAT(AEATType aeat) {
        effectiveTextField.setText(aeat.getAEA().get(0).getHeader().getEffective().toString()); //effectiveTextField에 날짜를 string으로 출력
        expiresTextField.setText(aeat.getAEA().get(0).getHeader().getExpires().toString()); //expiresTextField에 날짜를 string으로 출력
        eventPanel1.loadAEAT(aeat); //eventPanel에 데이터를 출력하기 위해 EventPanel class의 loadAEAT method 호출
        locationPanel2.loadAEAT(aeat); //LocationPanel 데이터를 출력하기 위해 LocationPanel class의 loadAEAT method 호출
    }
